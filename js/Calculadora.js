/* Creamos una clase donde se encuentran las operacioens básicas de una calculadora */
class Calculadora{
     /* La clase solo contiene métodos que resuleven los cálculos */
     sumar(num1, num2){
          return num1+num2
     }

     restar(num1, num2){
          return num1-num2
     }

     multiplicar(num1, num2){
          return num1*num2
     }
     
     dividir(num1, num2){
          return num1/num2
     }



}
